#[derive(Debug, PartialEq, Clone)]
pub enum Token {
    Illegal(String),
    EOF,
    Identifier(String),
    Integer(i32),
    Assign,
    Plus,
    Minus,
    Bang,
    Asterisk,
    Slash,
    LT,
    GT,
    Comma,
    Semicolon,
    LParen,
    RParen,
    LBrace,
    RBrace,
    Function,
    Let,
    Return,
    True,
    False,
    If,
    Else,
    Equal,
    NotEqual,
}
